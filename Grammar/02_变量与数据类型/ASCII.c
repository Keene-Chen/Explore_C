#include <stdio.h>

int main(void)
{
    char ch = 0;

    printf("blank ch = %d\n", ch = ' ');
    printf("0 ch = %d\n", ch = '0');
    printf("A ch = %d\n", ch = 'A');
    printf("a ch = %d\n", ch = 'a');
    printf("\\n ch = %d\n", ch = '\n');
    printf("\\0 ch = %d\n", ch = '\0');

    return 0;
}
